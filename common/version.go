package common

import (
	"fmt"
	"runtime/debug"
	"time"
)

var Version = "dev"

// GetVersion returns the version of the CLI
func GetVersion() string {
	if Version != "" && Version != "dev" {
		return Version
	}

	if info, ok := debug.ReadBuildInfo(); ok && info.Main.Version != "(devel)" {
		return info.Main.Version
	}
	return "dev"
}

// GenerateExportHeader returns a standardized header for exported files
func GenerateExportHeader() string {
	return fmt.Sprintf("# Generated by EPOS open-source CLI\n# Version: %s\n# Timestamp: %s\n#\n",
		GetVersion(),
		time.Now().UTC().Format(time.RFC3339))
}
